const command = process.argv[2]?.toLowerCase() || "help";

switch (command) {
  case "setup":
    cmdSetup();
    break;
  case "populate-ogl":
    cmdPopulate();
    break;
  case "populate-dw":
    cmdPopulateDw();
    break;
  case "populate-brp":
    cmdPopulateBrp();
    break;
  case "populate-5ecompatible":
    cmdPopulate5ecompatible();
    break;
  case "populate-orcus":
    cmdPopulateOrcus();
    break;
  case "help":
  case "--help":
  case "-h":
    cmdHelp();
    break;
  default:
    console.error(`Unknown command: ${command}`);
    cmdHelp();
    process.exit(1);
}
